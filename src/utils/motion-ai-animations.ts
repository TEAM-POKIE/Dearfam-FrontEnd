// 애니메이션 타입 정의
export type SpringType = "gentle" | "medium" | "strong";
export type BounceType = "fast" | "slow";

// 애니메이션 적용 헬퍼 함수들
export const applyMotionAI = {
  // 스프링 애니메이션 적용
  spring: (type: SpringType = "gentle") => ({
    transition: `transform ${MotionAI.spring[type]}`,
  }),

  // 바운스 애니메이션 적용
  bounce: (type: BounceType = "fast") => ({
    transition: `transform ${MotionAI.bounce[type]}`,
  }),

  // 스케일 애니메이션 (확대/축소)
  scale: (type: SpringType = "gentle") => ({
    transition: `transform ${MotionAI.spring[type]}`,
    transform: "scale(1.05)",
  }),

  // 슬라이드 애니메이션 (좌우 이동)
  slide: (type: SpringType = "medium") => ({
    transition: `transform ${MotionAI.spring[type]}`,
  }),

  // 페이드 + 스케일 조합
  fadeScale: (type: SpringType = "gentle") => ({
    transition: `opacity ${MotionAI.spring[type]}, transform ${MotionAI.spring[type]}`,
  }),
};

